type TempContextType = {
    selectBus: string;
    setSelectBus: (value: string) => void;
    groups: any[];
    selectGroup: any;
    menus: any;
};

export const TempContext = createContext<TempContextType>({
    selectBus: '',
    setSelectBus: (value: string) => null,
    groups: groups,
    selectGroup: groups[0],
    menus: menus,
});

export function TempContextProvider({
    children,
}: {
    children: React.ReactNode;
}) {
    const [selectBus, setSelectBus] = useState<string>('1호차');

    const selectGroup = useMemo(() => {
        return groups.find((group) => group.name === selectBus);
    }, [selectBus]);

    return (
        <TempContext.Provider
            value={{ selectBus, setSelectBus, groups, selectGroup, menus }}
        >
            {children}
        </TempContext.Provider>
    );
}

export function useTempContext() {
    return useContext(TempContext);
}
